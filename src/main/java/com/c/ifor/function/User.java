public class User {
    private String name;
    private int ruSiYear;
    private double salary;

    public User() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getRuSiYear() {
        return ruSiYear;
    }

    public void setRuSiYear(int ruSiYear) {
        this.ruSiYear = ruSiYear;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }
}
